package de.hssfds;

public class Punkt {

    private double x;
    private double y;

    public Punkt() {
        this.x = 0.0;
        this.y = 0.0;
    }
    public double getX() {
        return x;
    }
    public double getY() {
        return y;
    }
    public void setX(double param) {
        this.x = param;
    }
    public void setY(double param) {
        this.y = param;
    }
}